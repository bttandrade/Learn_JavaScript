// abs(x), retorna o valor absoluto de x
console.log(Math.abs(-10));

// sqrt(x), retorna a raiz quadrada de x
console.log(Math.sqrt(16));

// cbrt(x), retorna a raiz cúbica de x
console.log(Math.cbrt(64));

// ceil(x), retorna o valor aproximado de x para cima
console.log(Math.ceil(9.8));

// floor(x), retorna o valor aproximado de x para baixo
console.log(Math.floor(9.2));

// round(x), retorna o valor aproximado de x
console.log(Math.round(10.2));

// exp(x), retorna o valor de e (euler) ^ a x
console.log(Math.exp(2));

// log(x), retorna o logaritmo natural de x
console.log(Math.log(10));

// log(x), retorna o logaritmo na base 10 de x
console.log(Math.log10(10));

// log(x), retorna o logaritmo na base 2 de x
console.log(Math.log2(10));

// max(x, y, ...), retorna o maior valor de um grupo
console.log(Math.max(1, 2, 3));

// min(x, y, ...), retorna o menor valor de um grupo
console.log(Math.min(1, 2, 3));

// pow(x, y), retorna o valor de x exponencial de y
console.log(Math.pow(4, 2));

// random(), retorna um valor númerico randômico entre 0 e menor que 1
console.log(Math.random());

// sign(x), retorna se o número e positivo ou negativo ou zero
console.log(Math.sign(-10));

// sin(x), retorna o seno de x
console.log(Math.sin(45));

// cos(x), retorna o cosseno de x
console.log(Math.cos(45));

// tan(x), retorna a tangente de x
console.log(Math.tan(45));

// trunc(x), retorna a parte inteira de um número
console.log(Math.trunc(3.14));